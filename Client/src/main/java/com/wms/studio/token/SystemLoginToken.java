package com.wms.studio.token;

import org.apache.shiro.authc.UsernamePasswordToken;

/**
 * @author WMS
 * @version 1.0
 * @date 2014年8月10日 下午1:58:55
 */
@SuppressWarnings("serial")
public class SystemLoginToken extends UsernamePasswordToken {

	private String captcha;

	public SystemLoginToken() {
		super();
	}

	public SystemLoginToken(String username, char[] password,
			boolean rememberMe, String host, String captcha) {
		super(username, password, rememberMe, host);
		this.captcha = captcha;
	}

	/**
	 * @return the captcha
	 */
	public String getCaptcha() {
		return captcha;
	}

	/**
	 * @param captcha
	 *            the captcha to set
	 */
	public void setCaptcha(String captcha) {
		this.captcha = captcha;
	}
}
